    public void Preencher(String descricao) throws HeadlessException {

        try {
            if (descricao == null) {
                listatarefas = new TarefaDao().buscarTodos();
            } else {
                listatarefas = new TarefaDao().buscarPeloNome(descricao);
            }

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            int idx = dtm.getRowCount();

            for (int i = 0; i < idx; i++) {
                dtm.removeRow(0);
            }
            for (Tarefa ta : listatarefas) {
                Object[] row = {
                    ta.getIdtarefa(),
                    ta.getDescricao()};

                dtm.addRow(row);
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Tela_Boletim_Diario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Tela_Boletim_Diario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

