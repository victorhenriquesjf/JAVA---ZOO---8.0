    private void Preencher() throws HeadlessException {
        Preencher(null);
    }

    public void Preencher(String nome) throws HeadlessException {

        try {
            if (nome == null) {
                listaU = new UsuarioDao().buscarTodos();
            } else {
                listaU = new UsuarioDao().buscarPeloNome(nome);
            }

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            int idx = dtm.getRowCount();

            for (int i = 0; i < idx; i++) {
                dtm.removeRow(0);
            }
            for (Usuario u : listaU) {
                Object[] row = {
                    u.getIdusuario(),
                    u.getNome(),
                    u.getLogin(),
                    u.getSenha(),
                    u.getTipousuario()};

                dtm.addRow(row);
            }

        } catch (Exception ex) {
            Logger.getLogger(Tela_Boletim_Diario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
