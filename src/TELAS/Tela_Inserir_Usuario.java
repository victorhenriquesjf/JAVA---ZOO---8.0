    private void aosalvar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aosalvar
        Usuario u = new Usuario();

        UsuarioDao dao = new UsuarioDao();

        if (jComboBox2.getSelectedIndex() != 1 && jComboBox2.getSelectedIndex() != 2 && jComboBox2.getSelectedIndex() != 3) {

            JOptionPane.showMessageDialog(this, "Por favor preencha o tipo de usuario");

        } else {

            u.setIdusuario(0);
            u.setNome(jTextField1.getText());
            u.setLogin(jTextField3.getText());
            u.setSenha(jTextField4.getText());
            u.setTipousuario((Integer) jComboBox2.getSelectedIndex());

        }

        if (u.getNome() != null) {
            try {
                dao.inserir(u);
                JOptionPane.showMessageDialog(rootPane, "Cadastrado com sucesso");

            } catch (Exception ex) {
                Logger.getLogger(Tela_Inserir_Usuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        Preencher();
    }//GEN-LAST:event_aosalvar

    private void aoexcluirusuario(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aoexcluirusuario
        if (jTable1.getSelectedRowCount() == 1) {
            if (JOptionPane.showConfirmDialog(this, "Deseja Apagar o usuario" + "?", "Atenção",
                    JOptionPane.YES_NO_OPTION + JOptionPane.ERROR_MESSAGE) == JOptionPane.YES_OPTION) {
                int idusuario = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);

                Usuario u = new Usuario(idusuario);

                try {
                    new UsuarioDao().apagar(u);
                    JOptionPane.showMessageDialog(null, "Apagado com sucesso!");
                    Preencher();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Não conseguiu apagar " + ex.getMessage());
                }
            }
            Preencher();
        }
    }//GEN-LAST:event_aoexcluirusuario

